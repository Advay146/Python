def add(x,y):
    return x+y
def subtract(x,y):
    return x-y
def division(x,y):
    return x/y
def multiply(x,y):
    return x*y
num1=int(input("Enter number 1 :"))
num2=int(input("Enter number 2 :"))

print("Sum :",add(num1,num2))
print("Subtraction :", subtract(num1, num2))
print("Division :",division(num1,num2))
print("Multiplication:", multiply(num1, num2))